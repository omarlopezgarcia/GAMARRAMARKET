/* Crear tabla PERSONA */
CREATE TABLE persona
(
   IDPER INT GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 200 INCREMENT BY 1,
   DNIPER CHAR(8),
   NOMPER VARCHAR2(60),
   APEPER VARCHAR2(80),
   EMAPER VARCHAR2(80),
   CELPER CHAR(9),
   TIPPER CHAR(1),
   ESTPER CHAR(1) DEFAULT 'A',
   FECNACPER DATE,
   CONSTRAINT PER_PK PRIMARY KEY (IDPER)       
);
COMMIT;

/* Crear tabla VENTA */
CREATE TABLE venta
(
   IDVEN INT GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
   FECVEN DATE,
   IDCLI INT,
   IDVEND INT,
   TIPPAGVEN CHAR(1),
   ESTVEN CHAR(1) DEFAULT 'A',
   CONSTRAINT VEN_PK PRIMARY KEY (IDVEN)       
);
COMMIT;

/* Crear tabla VENTA_DETALLE */
CREATE TABLE venta_detalle
(
   IDVENDET INT GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 300 INCREMENT BY 1,
   IDVEN INT,
   CODPROD VARCHAR2(3),
   CANVENDET INT,
   CONSTRAINT VENDET_PK PRIMARY KEY (IDVENDET)       
);
COMMIT;

/* Crear tabla PRODUCTO */
CREATE TABLE producto   
(
   CODPROD VARCHAR2(3),
   NOMPRO VARCHAR2(50),
   IDCAT INT,
   PREPRO NUMBER(8,2),
   STOCKPRO INT,
   ESTPRO CHAR(1)  DEFAULT 'A',
   CONSTRAINT PROD_PK PRIMARY KEY (CODPROD)       
);
COMMIT;

/* Crear tabla CATEGORIA */
CREATE TABLE categoria   
(
   IDCAT INT GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 10 INCREMENT BY 10,
   NOMCAT VARCHAR2(60),
   CONSTRAINT CAT_PK PRIMARY KEY (IDCAT)       
);
COMMIT;

/* Relacionar tabla VENTA con PERSONA (Cliente) */
ALTER TABLE VENTA 
      ADD CONSTRAINT IDCLI_FK FOREIGN KEY (IDCLI)
      REFERENCES PERSONA (IDPER);
COMMIT;

/* Relacionar tabla VENTA con PERSONA (vendedor) */
ALTER TABLE VENTA 
      ADD CONSTRAINT IDVEND_FK FOREIGN KEY (IDVEND)
      REFERENCES PERSONA (IDPER);
